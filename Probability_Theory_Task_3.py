"""
Задача 3
На соревновании по биатлону один из трех спортсменов стреляет и попадает в мишень.
Вероятность попадания для первого спортсмена равна 0.9, для второго — 0.8, для третьего — 0.6.
Найти вероятность того, что выстрел произведен: a). Первым спортсменом б). Вторым спортсменом в). Третьим спортсменом.
"""
# Вероятности попадания для каждого спортсмена
P_shooter1 = 0.9
P_shooter2 = 0.8
P_shooter3 = 0.6

# Вероятности того, что выстрел сделан каждым спортсменом (равны, так как спортсмены выбираются случайно)
P1 = P2 = P3 = 1/3

# Полная вероятность попадания
P_hit = (P_shooter1 * P1) + (P_shooter2 * P2) + (P_shooter3 * P3)

# Вероятность того, что выстрел сделан каждым спортсменом при условии попадания
P_1_given_hit = (P_shooter1 * P1) / P_hit
P_2_given_hit = (P_shooter2 * P2) / P_hit
P_3_given_hit = (P_shooter3 * P3) / P_hit

print(f"Вероятность, что выстрел сделан первым спортсменом: {P_1_given_hit:.2f}")
print(f"Вероятность, что выстрел сделан вторым спортсменом: {P_2_given_hit:.2f}")
print(f"Вероятность, что выстрел сделан третьим спортсменом: {P_3_given_hit:.2f}")
